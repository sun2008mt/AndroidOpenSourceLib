package whu.sun.androidopensourcelibrarytest;

public class Func {

    private int imgId;
    private String name;

    public Func(int imgId, String name) {
        this.imgId = imgId;
        this.name = name;
    }

    public int getImgId() {
        return imgId;
    }

    public String getName() {
        return name;
    }
}
